// ── Main entry point ────────────────────────────────────────────────────────────

/**
 * Run the weekly error digest.
 *
 * Queries Sentry for new issues, creates a GitHub issue with the summary,
 * and writes to the dead letter table on failure.
 *
 * Designed to be called from an Inngest scheduled function or standalone tests.
 *
 * Required env vars:
 *   SENTRY_API_URL         — e.g., https://sentry.getplexo.com
 *   SENTRY_AUTH_TOKEN       — Sentry internal API token
 *   SENTRY_ORG_SLUG         — e.g., joeybuilt
 *   SENTRY_PROJECT_SLUG     — e.g., plexo
 *   GITHUB_TOKEN            — GitHub PAT with repo:issues scope
 *   GITHUB_DIGEST_OWNER     — e.g., joeybuilt-official
 *   GITHUB_DIGEST_REPO      — e.g., plexo
 */
export async function runWeeklyDigest(): Promise<DigestResult> {
    const weekOf = new Date().toISOString().slice(0, 10)
    const maxRetries = 3
    const backoffMinutes = 15

    for (let attempt = 1; attempt <= maxRetries; attempt++) {
        try {
            logger.info({ weekOf, attempt }, 'Running weekly error digest')

            // 1. Query Sentry
            const issues = await querySentryIssues({
                sentryApiUrl: process.env.SENTRY_API_URL ?? 'https://sentry.getplexo.com',
                sentryAuthToken: process.env.SENTRY_AUTH_TOKEN ?? '',
                sentryOrgSlug: process.env.SENTRY_ORG_SLUG ?? 'joeybuilt',
                sentryProjectSlug: process.env.SENTRY_PROJECT_SLUG ?? 'plexo',
                minOccurrences: 3,
                maxItems: 30,
            })

            // 2. Format digest body
            const body = formatDigestBody(issues, weekOf)

            // 3. Create GitHub issue
            const githubIssueUrl = await createGitHubIssue({
                githubToken: process.env.GITHUB_TOKEN ?? '',
                owner: process.env.GITHUB_DIGEST_OWNER ?? 'joeybuilt-official',
                repo: process.env.GITHUB_DIGEST_REPO ?? 'plexo',
                title: `Weekly Error Digest — ${weekOf}`,
                body,
                labels: ['auto-triage', 'from-sentry'],
            })

            logger.info({ weekOf, issueCount: issues.length, githubIssueUrl }, 'Weekly digest completed')

            return {
                issueCount: issues.length,
                githubIssueUrl,
                error: null,
            }
        } catch (err) {
            const errorMsg = err instanceof Error ? err.message : String(err)
            logger.error({ weekOf, attempt, err: errorMsg }, 'Weekly digest attempt failed')

            if (attempt === maxRetries) {
                // Final attempt failed — write to dead letter table
                await writeDeadLetter({
                    weekOf,
                    attempt,
                    error: errorMsg,
                })

                return {
                    issueCount: 0,
                    githubIssueUrl: null,
                    error: `All ${maxRetries} attempts failed. Last error: ${errorMsg}`,
                }
            }

            // Wait before retrying (exponential backoff)
            const delayMs = backoffMinutes * 60_000 * attempt
            logger.info({ nextAttemptIn: `${backoffMinutes * attempt}m` }, 'Retrying after backoff')
            await new Promise((resolve) => setTimeout(resolve, delayMs))
        }
    }

    // Unreachable, but TypeScript needs it
    return { issueCount: 0, githubIssueUrl: null, error: 'Unexpected exit from retry loop' }
}
