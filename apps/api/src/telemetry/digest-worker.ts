// ── GitHub issue creation ───────────────────────────────────────────────────────

/**
 * Format issues into a markdown table for the GitHub issue body.
 */
function formatDigestBody(issues: SentryIssueGroup[], weekOf: string): string {
    if (issues.length === 0) {
        return `# Weekly Error Digest — ${weekOf}\n\nNo new issues with >= 3 occurrences this week.`
    }

    const rows = issues.map((i) =>
        `| [${i.title.slice(0, 80)}](${i.permalink}) | ${i.culprit.slice(0, 40)} | ${i.count} | ${i.lastSeen.slice(0, 10)} |`,
    )

    return [
        `# Weekly Error Digest — ${weekOf}`,
        '',
        `${issues.length} issue(s) with >= 3 occurrences in the past 7 days.`,
        '',
        '| Issue | Culprit | Count | Last Seen |',
        '|-------|---------|-------|-----------|',
        ...rows,
        '',
        '*Auto-generated by the telemetry digest worker.*',
    ].join('\n')
}

